#include "libft.h"

int	ft_atoi(const char *str)
{	
	int		sign;
	int		base_conv;
	size_t	i;

	sign = 1;
	base_conv = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
	{
		sign *= -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		base_conv = base_conv * 10 + (str[i] - '0');
		i++;
	}
	return (base_conv * sign);
}
